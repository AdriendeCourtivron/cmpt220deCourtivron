
public class Players 
{
	String name = "Player";
	int goals = 0;
	String team = "Team";
 
	Players ()
	{
		
	}

	public String getName() 
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public int getGoals() 
	{
		return goals;
	}

	public void setGoals(int goals) 
	{
		this.goals = goals;
	}

	public String getTeam() 
	{
		return team;
	}

	public void setTeam(String team)
	{
		this.team = team;
	}

}
